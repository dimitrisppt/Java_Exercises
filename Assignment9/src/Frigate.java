/**
 * The class Frigate extends the class Battleship, and inherits all of its methods.
 */
public class Frigate extends Battleship {
	/**
	 * The constructor Frigate receives two parameters: row and shipSize of type integer.
	 */
	public Frigate (int row, int shipSize) {
		// The super keyword differentiates the method in the superclass from the method in the subclass. 
		super(row, shipSize);
		
	}
}
